package com.azure.resourcemanager.cdn.implementation;

import com.azure.core.http.rest.PagedIterable;
import com.azure.resourcemanager.cdn.fluent.models.AfdEndpointInner;
import com.azure.resourcemanager.cdn.models.AfdEndpoint;
import com.azure.resourcemanager.cdn.models.AfdEndpointUpdateParameters;
import com.azure.resourcemanager.cdn.models.AfdProvisioningState;
import com.azure.resourcemanager.cdn.models.AfdPurgeParameters;
import com.azure.resourcemanager.cdn.models.AutoGeneratedDomainNameLabelScope;
import com.azure.resourcemanager.cdn.models.CdnProfile;
import com.azure.resourcemanager.cdn.models.CustomDomainValidationResult;
import com.azure.resourcemanager.cdn.models.DeploymentStatus;
import com.azure.resourcemanager.cdn.models.EnabledState;
import com.azure.resourcemanager.cdn.models.Usage;
import com.azure.resourcemanager.cdn.models.Route;
import com.azure.resourcemanager.cdn.models.ValidateCustomDomainInput;
import com.azure.resourcemanager.resources.fluentcore.arm.models.implementation.ExternalChildResourceImpl;
import com.azure.resourcemanager.resources.fluentcore.utils.PagedConverter;
import reactor.core.publisher.Mono;

import java.util.List;
import java.util.Map;
import java.util.Objects;

/**
 * Implementation for {@link AfdEndpoint}.
 */
class AfdEndpointImpl extends ExternalChildResourceImpl<AfdEndpoint, AfdEndpointInner, CdnProfileImpl, CdnProfile>
    implements AfdEndpoint, AfdEndpoint.DefinitionStages.Blank<CdnProfile.DefinitionStages.WithStandardCreate>,
    AfdEndpoint.DefinitionStages.WithAttach<CdnProfile.DefinitionStages.WithStandardCreate>,
    AfdEndpoint.UpdateDefinitionStages.Blank<CdnProfile.Update>,
    AfdEndpoint.UpdateDefinitionStages.WithAttach<CdnProfile.Update>, AfdEndpoint.Update {

    private final RoutesImpl routes;

    AfdEndpointImpl(String name, CdnProfileImpl parent, AfdEndpointInner inner) {
        super(name, parent, inner);
        this.routes = new RoutesImpl(this);
    }

    @Override
    public String id() {
        return this.innerModel().id();
    }

    @Override
    public EnabledState enabledState() {
        return this.innerModel().enabledState();
    }

    @Override
    public AutoGeneratedDomainNameLabelScope autoGeneratedDomainNameLabelScope() {
        return this.innerModel().autoGeneratedDomainNameLabelScope();
    }

    @Override
    public AfdProvisioningState provisioningState() {
        return this.innerModel().provisioningState();
    }

    @Override
    public DeploymentStatus deploymentStatus() {
        return this.innerModel().deploymentStatus();
    }

    @Override
    public String hostname() {
        return this.innerModel().hostname();
    }

    @Override
    public void purgeContent(List<String> contentPaths) {
        this.purgeContent(contentPaths, null);
    }

    @Override
    public void purgeContent(List<String> contentPaths, List<String> domains) {
        this.purgeContentAsync(contentPaths, domains).block();
    }

    @Override
    public Mono<Void> purgeContentAsync(List<String> contentPaths) {
        return purgeContentAsync(contentPaths, null);
    }

    @Override
    public Mono<Void> purgeContentAsync(List<String> contentPaths, List<String> domains) {
        AfdPurgeParameters parameters = buildPurgeParameters(contentPaths, domains);
        return this.parent()
            .manager()
            .serviceClient()
            .getAfdEndpoints()
            .purgeContentAsync(this.parent().resourceGroupName(), this.parent().name(), this.name(), parameters);
    }

    @Override
    public CustomDomainValidationResult validateCustomDomain(String hostName) {
        return this.validateCustomDomainAsync(hostName).block();
    }

    @Override
    public Mono<CustomDomainValidationResult> validateCustomDomainAsync(String hostName) {
        Objects.requireNonNull(hostName, "'hostName' cannot be null.");
        ValidateCustomDomainInput input = new ValidateCustomDomainInput().withHostname(hostName);
        return this.parent()
            .manager()
            .serviceClient()
            .getAfdEndpoints()
            .validateCustomDomainAsync(this.parent().resourceGroupName(), this.parent().name(), this.name(), input)
            .map(CustomDomainValidationResult::new);
    }

    @Override
    public PagedIterable<Usage> listResourceUsage() {
        return PagedConverter.mapPage(
            this.parent()
                .manager()
                .serviceClient()
                .getAfdEndpoints()
                .listResourceUsage(this.parent().resourceGroupName(), this.parent().name(), this.name()),
            UsageImpl::new);
    }

    @Override
    public Map<String, Route> routes() {
        return this.routes.routesAsMap();
    }

    @Override
    public Mono<AfdEndpoint> createResourceAsync() {
        final AfdEndpointImpl self = this;
        return this.parent()
            .manager()
            .serviceClient()
            .getAfdEndpoints()
            .createAsync(this.parent().resourceGroupName(), this.parent().name(), this.name(), this.innerModel())
            .map(inner -> {
                self.setInner(inner);
                return self;
            });
    }

    @Override
    public Mono<AfdEndpoint> updateResourceAsync() {
        final AfdEndpointImpl self = this;
        AfdEndpointUpdateParameters parameters = new AfdEndpointUpdateParameters().withTags(this.innerModel().tags())
            .withEnabledState(this.innerModel().enabledState());
        return this.parent()
            .manager()
            .serviceClient()
            .getAfdEndpoints()
            .updateAsync(this.parent().resourceGroupName(), this.parent().name(), this.name(), parameters)
            .map(inner -> {
                self.setInner(inner);
                return self;
            });
    }

    @Override
    public Mono<Void> deleteResourceAsync() {
        return this.parent()
            .manager()
            .serviceClient()
            .getAfdEndpoints()
            .deleteAsync(this.parent().resourceGroupName(), this.parent().name(), this.name());
    }

    @Override
    protected Mono<AfdEndpointInner> getInnerAsync() {
        return this.parent()
            .manager()
            .serviceClient()
            .getAfdEndpoints()
            .getAsync(this.parent().resourceGroupName(), this.parent().name(), this.name());
    }

    @Override
    protected Mono<Void> afterPostRunAsync(boolean isGroupFaulted) {
        this.routes.clear();
        return Mono.empty();
    }

    @Override
    public CdnProfileImpl attach() {
        return this.parent().withAfdEndpoint(this);
    }

    @Override
    public AfdEndpointImpl withEnabledState(EnabledState enabledState) {
        this.innerModel().withEnabledState(enabledState);
        return this;
    }

    @Override
    public AfdEndpointImpl withAutoGeneratedDomainNameLabelScope(AutoGeneratedDomainNameLabelScope scope) {
        this.innerModel().withAutoGeneratedDomainNameLabelScope(scope);
        return this;
    }

    // ---- Nested route CRUD ----

    @Override
    public RouteImpl defineRoute(String name) {
        return this.routes.defineNewRoute(name);
    }

    @Override
    public RouteImpl updateRoute(String name) {
        return this.routes.updateRoute(name);
    }

    @Override
    public AfdEndpointImpl withoutRoute(String name) {
        this.routes.remove(name);
        return this;
    }

    AfdEndpointImpl withRoute(RouteImpl route) {
        this.routes.addRoute(route);
        return this;
    }

    private static AfdPurgeParameters buildPurgeParameters(List<String> contentPaths, List<String> domains) {
        if (contentPaths == null || contentPaths.isEmpty()) {
            throw new IllegalArgumentException("contentPaths cannot be null or empty.");
        }
        AfdPurgeParameters parameters = new AfdPurgeParameters().withContentPaths(contentPaths);
        if (domains != null && !domains.isEmpty()) {
            parameters.withDomains(domains);
        }
        return parameters;
    }
}
