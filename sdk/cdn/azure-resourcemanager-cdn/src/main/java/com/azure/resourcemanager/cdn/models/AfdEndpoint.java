package com.azure.resourcemanager.cdn.models;

import com.azure.core.annotation.Fluent;
import com.azure.core.http.rest.PagedIterable;
import com.azure.resourcemanager.cdn.fluent.models.AfdEndpointInner;
import com.azure.resourcemanager.resources.fluentcore.arm.models.ExternalChildResource;
import com.azure.resourcemanager.resources.fluentcore.model.HasInnerModel;
import com.azure.resourcemanager.resources.fluentcore.model.Settable;
import reactor.core.publisher.Mono;

import java.util.List;
import java.util.Map;

/**
 * An immutable client-side representation of an Azure Front Door (AFD) endpoint that lives under a {@link CdnProfile}.
 */
@Fluent
public interface AfdEndpoint extends ExternalChildResource<AfdEndpoint, CdnProfile>, HasInnerModel<AfdEndpointInner> {

    /**
     * Gets the enablement state of the endpoint.
     *
     * @return the enablement state of the endpoint
     */
    EnabledState enabledState();

    /**
     * Gets the scope that determines whether a generated host name can be reused.
     *
     * @return the scope that determines whether a generated host name can be reused
     */
    AutoGeneratedDomainNameLabelScope autoGeneratedDomainNameLabelScope();

    /**
     * Gets the provisioning state reported by the service.
     *
     * @return the provisioning state reported by the service
     */
    AfdProvisioningState provisioningState();

    /**
     * Gets the deployment status for the endpoint.
     *
     * @return the deployment status for the endpoint
     */
    DeploymentStatus deploymentStatus();

    /**
     * Gets the host name assigned to the endpoint.
     *
     * @return the host name assigned to the endpoint
     */
    String hostname();

    /**
     * Purges cached content for the endpoint.
     *
     * @param contentPaths paths that should be removed from the cache
     */
    void purgeContent(List<String> contentPaths);

    /**
     * Purges cached content for the endpoint.
     *
     * @param contentPaths paths that should be removed from the cache
     * @param domains optional list of linked domains to scope the purge to
     */
    void purgeContent(List<String> contentPaths, List<String> domains);

    /**
     * Purges cached content for the endpoint asynchronously.
     *
     * @param contentPaths paths that should be removed from the cache
     * @return a representation of the deferred computation of this call
     */
    Mono<Void> purgeContentAsync(List<String> contentPaths);

    /**
     * Purges cached content for the endpoint asynchronously.
     *
     * @param contentPaths paths that should be removed from the cache
     * @param domains optional list of linked domains to scope the purge to
     * @return a representation of the deferred computation of this call
     */
    Mono<Void> purgeContentAsync(List<String> contentPaths, List<String> domains);

    /**
     * Validates that the provided custom domain maps correctly to this endpoint.
     *
     * @param hostName the host name that should point to this endpoint
     * @return the validation result
     */
    CustomDomainValidationResult validateCustomDomain(String hostName);

    /**
     * Validates that the provided custom domain maps correctly to this endpoint asynchronously.
     *
     * @param hostName the host name that should point to this endpoint
     * @return a representation of the deferred computation of this call
     */
    Mono<CustomDomainValidationResult> validateCustomDomainAsync(String hostName);

    /**
     * Lists usage information for the endpoint.
     *
     * @return usage information for the endpoint
     */
    PagedIterable<Usage> listResourceUsage();

    /**
     * Gets latest routes by sending http request.
     *
     * @return routes in this endpoint, indexed by name
     */
    Map<String, Route> routes();

    /**
     * Grouping of AFD endpoint definition stages as part of a parent {@link CdnProfile} definition.
     */
    interface DefinitionStages {
        /**
         * The first stage of an AFD endpoint definition.
         *
         * @param <ParentT> the stage of the parent CDN profile definition to return to after attaching this definition
         */
        interface Blank<ParentT> extends WithAttach<ParentT> {
            /**
             * Specifies whether the endpoint should be enabled when created.
             *
             * @param enabledState the desired enablement state
             * @return the next stage of the definition
             */
            Blank<ParentT> withEnabledState(EnabledState enabledState);

            /**
             * Sets the reuse scope for the auto-generated domain label.
             *
             * @param scope the label reuse scope
             * @return the next stage of the definition
             */
            Blank<ParentT> withAutoGeneratedDomainNameLabelScope(AutoGeneratedDomainNameLabelScope scope);
        }

        /**
         * The stage of the definition containing all optional settings prior to attachment.
         *
         * @param <ParentT> the stage of the parent CDN profile definition to return to after attaching this definition
         */
        interface WithAttach<ParentT> extends Attachable<ParentT> {
            /**
             * Starts the definition of a new route to be attached to this endpoint.
             *
             * @param name a new route name
             * @return the first stage of a new route definition
             */
            Route.DefinitionStages.Blank<WithAttach<ParentT>> defineRoute(String name);
        }

        /**
         * The final stage of an AFD endpoint definition.
         *
         * @param <ParentT> the stage of the parent CDN profile definition to return to after attaching this definition
         */
        interface Attachable<ParentT> {
            /**
             * Attaches the defined endpoint to the parent CDN profile.
             *
             * @return the next stage of the parent definition
             */
            ParentT attach();
        }
    }

    /**
     * The entirety of an AFD endpoint definition.
     *
     * @param <ParentT> the stage of the parent CDN profile definition to return to after attaching this definition
     */
    interface Definition<ParentT> extends DefinitionStages.Blank<ParentT> {
    }

    /**
     * Grouping of AFD endpoint definition stages that run as part of a {@link CdnProfile#update()} flow.
     */
    interface UpdateDefinitionStages {
        /**
         * The first stage of an AFD endpoint definition inside a profile update.
         *
         * @param <ParentT> the stage of the parent CDN profile definition to return to after attaching this definition
         */
        interface Blank<ParentT> extends WithAttach<ParentT> {
            /**
             * Specifies whether the endpoint should be enabled when created.
             *
             * @param enabledState the desired enablement state
             * @return the next stage of the definition
             */
            Blank<ParentT> withEnabledState(EnabledState enabledState);
        }

        /**
         * The stage of the definition containing all optional settings prior to attachment.
         *
         * @param <ParentT> the stage of the parent CDN profile definition to return to after attaching this definition
         */
        interface WithAttach<ParentT> extends Attachable<ParentT> {
        }

        /**
         * The final stage of an AFD endpoint definition inside a profile update.
         *
         * @param <ParentT> the stage of the parent CDN profile definition to return to after attaching this definition
         */
        interface Attachable<ParentT> {
            /**
             * Attaches the defined endpoint to the parent CDN profile update.
             *
             * @return the next stage of the parent update
             */
            ParentT attach();
        }
    }

    /**
     * The entirety of an AFD endpoint update inside a {@link CdnProfile#update()} flow.
     */
    interface Update extends Settable<CdnProfile.Update> {
        /**
         * Specifies whether the endpoint should be enabled.
         *
         * @param enabledState the desired enablement state
         * @return the next stage of the update
         */
        Update withEnabledState(EnabledState enabledState);

        /**
         * Starts the definition of a new route to be attached to this endpoint.
         *
         * @param name a new route name
         * @return the first stage of a new route definition
         */
        Route.UpdateDefinitionStages.Blank<Update> defineRoute(String name);

        /**
         * Begins the description of an update of an existing route in this endpoint.
         *
         * @param name the name of an existing route
         * @return the first stage of the update of the route
         */
        Route.Update updateRoute(String name);

        /**
         * Removes a route from this endpoint.
         *
         * @param name the name of an existing route
         * @return the next stage of the endpoint update
         */
        Update withoutRoute(String name);
    }
}
