package com.azure.spring.data.cosmos.repository.integration;

import com.azure.spring.data.cosmos.ReactiveIntegrationTestCollectionManager;
import com.azure.spring.data.cosmos.core.ReactiveCosmosTemplate;
import com.azure.spring.data.cosmos.domain.AuditableEntity;
import com.azure.spring.data.cosmos.domain.AuditableIdGeneratedEntity;
import com.azure.spring.data.cosmos.repository.StubDateTimeProvider;
import com.azure.spring.data.cosmos.repository.StubReactiveAuditorProvider;
import com.azure.spring.data.cosmos.repository.TestRepositoryReactiveAuditingConfig;
import com.azure.spring.data.cosmos.repository.repository.ReactiveAuditableIdGeneratedRepository;
import com.azure.spring.data.cosmos.repository.repository.ReactiveAuditableRepository;
import org.assertj.core.util.Lists;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit.jupiter.SpringExtension;
import reactor.core.publisher.Flux;
import reactor.core.publisher.Mono;
import reactor.test.StepVerifier;

import java.time.OffsetDateTime;
import java.time.ZoneId;
import java.time.temporal.ChronoUnit;
import java.util.UUID;

import static org.assertj.core.api.Assertions.assertThat;

/**
 * Integration tests for reactive auditing using {@code @EnableReactiveCosmosAuditing}
 * with {@code ReactiveAuditorAware}.
 */
@ExtendWith(SpringExtension.class)
@ContextConfiguration(classes = TestRepositoryReactiveAuditingConfig.class)
public class ReactiveAuditableWithReactiveAuditorIT {

    public static final ReactiveIntegrationTestCollectionManager collectionManager =
        new ReactiveIntegrationTestCollectionManager();

    @Autowired
    private ReactiveCosmosTemplate template;
    @Autowired
    private ReactiveAuditableRepository auditableRepository;
    @Autowired
    private ReactiveAuditableIdGeneratedRepository reactiveAuditableIdGeneratedRepository;
    @Autowired
    private StubDateTimeProvider stubDateTimeProvider;
    @Autowired
    private StubReactiveAuditorProvider stubReactiveAuditorProvider;

    @BeforeEach
    public void setup() {
        collectionManager.ensureContainersCreatedAndEmpty(template, AuditableEntity.class,
            AuditableIdGeneratedEntity.class);
    }

    @Test
    public void testInsertShouldSetAuditableEntriesWithReactiveAuditor() {
        final AuditableEntity entity = new AuditableEntity();
        entity.setId(UUID.randomUUID().toString());
        final OffsetDateTime now = OffsetDateTime.now(ZoneId.of("UTC")).truncatedTo(ChronoUnit.MICROS);

        stubDateTimeProvider.setNow(now);
        stubReactiveAuditorProvider.setCurrentAuditor("reactive-created-by");
        final Mono<AuditableEntity> savedEntity = auditableRepository.save(entity);
        StepVerifier
            .create(savedEntity)
            .expectNextMatches(actual -> validateAuditableFields(actual,
                "reactive-created-by",
                now,
                "reactive-created-by",
                now))
            .verifyComplete();
    }

    @Test
    public void testUpdateShouldNotOverwriteCreatedEntriesWithReactiveAuditor() {
        final AuditableEntity entity = new AuditableEntity();
        entity.setId(UUID.randomUUID().toString());
        final OffsetDateTime createdOn = OffsetDateTime.now(ZoneId.of("UTC")).truncatedTo(ChronoUnit.MICROS);

        stubDateTimeProvider.setNow(createdOn);
        stubReactiveAuditorProvider.setCurrentAuditor("reactive-created-by");
        final AuditableEntity saved = auditableRepository.save(entity).block();

        final OffsetDateTime modifiedOn = createdOn.plusMinutes(1);
        stubDateTimeProvider.setNow(modifiedOn);
        stubReactiveAuditorProvider.setCurrentAuditor("reactive-modified-by");
        final Mono<AuditableEntity> modified = auditableRepository.save(saved);
        StepVerifier
            .create(modified)
            .expectNextMatches(actual -> validateAuditableFields(actual,
                "reactive-created-by",
                createdOn,
                "reactive-modified-by",
                modifiedOn))
            .verifyComplete();
    }

    @Test
    public void testInsertShouldSetAuditableEntriesIfIdAutoGeneratedWithReactiveAuditor() {
        final AuditableIdGeneratedEntity entity = new AuditableIdGeneratedEntity();
        final OffsetDateTime now = OffsetDateTime.now(ZoneId.of("UTC")).truncatedTo(ChronoUnit.MICROS);

        stubDateTimeProvider.setNow(now);
        stubReactiveAuditorProvider.setCurrentAuditor("reactive-created-by");
        final Mono<AuditableIdGeneratedEntity> savedEntity = reactiveAuditableIdGeneratedRepository.save(entity);

        StepVerifier
            .create(savedEntity)
            .expectNextMatches(actual -> validateAuditableFields(actual,
                "reactive-created-by", now,
                "reactive-created-by", now))
            .verifyComplete();
    }

    @Test
    public void testInsertAllShouldSetAuditableEntriesIfIdAutoGeneratedWithReactiveAuditor() {
        final AuditableIdGeneratedEntity entity1 = new AuditableIdGeneratedEntity();
        final AuditableIdGeneratedEntity entity2 = new AuditableIdGeneratedEntity();
        final OffsetDateTime now = OffsetDateTime.now(ZoneId.of("UTC")).truncatedTo(ChronoUnit.MICROS);

        stubDateTimeProvider.setNow(now);
        stubReactiveAuditorProvider.setCurrentAuditor("reactive-created-by");
        final Flux<AuditableIdGeneratedEntity> savedEntities = reactiveAuditableIdGeneratedRepository
            .saveAll(Lists.newArrayList(entity1, entity2));

        StepVerifier
            .create(savedEntities)
            .expectNextMatches(actual -> validateAuditableFields(actual,
                "reactive-created-by", now,
                "reactive-created-by", now))
            .expectNextMatches(actual -> validateAuditableFields(actual,
                "reactive-created-by", now,
                "reactive-created-by", now))
            .verifyComplete();
    }

    private boolean validateAuditableFields(AuditableEntity entity,
                                            String expectedCreatedBy, OffsetDateTime expectedCreatedDate,
                                            String expectedModifiedBy, OffsetDateTime expectedModifiedTime) {
        assertThat(entity.getCreatedBy()).isEqualTo(expectedCreatedBy);
        assertThat(entity.getCreatedDate()).isEqualTo(expectedCreatedDate);
        assertThat(entity.getLastModifiedBy()).isEqualTo(expectedModifiedBy);
        assertThat(entity.getLastModifiedByDate()).isEqualTo(expectedModifiedTime);
        return true;
    }

    private boolean validateAuditableFields(AuditableIdGeneratedEntity entity,
                                            String expectedCreatedBy, OffsetDateTime expectedCreatedDate,
                                            String expectedModifiedBy, OffsetDateTime expectedModifiedTime) {
        assertThat(entity.getCreatedBy()).isEqualTo(expectedCreatedBy);
        assertThat(entity.getCreatedDate()).isEqualTo(expectedCreatedDate);
        assertThat(entity.getLastModifiedBy()).isEqualTo(expectedModifiedBy);
        assertThat(entity.getLastModifiedByDate()).isEqualTo(expectedModifiedTime);
        return true;
    }
}
