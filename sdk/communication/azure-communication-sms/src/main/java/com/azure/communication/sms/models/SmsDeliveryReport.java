package com.azure.communication.sms.models;

import com.azure.core.annotation.Immutable;
import java.time.OffsetDateTime;
import java.util.List;

/**
 * Delivery report for an outgoing SMS message.
 */
@Immutable
public final class SmsDeliveryReport {
    private final SmsDeliveryStatus deliveryStatus;
    private final String deliveryStatusDetails;
    private final List<SmsDeliveryAttempt> deliveryAttempts;
    private final OffsetDateTime receivedTimestamp;
    private final String tag;
    private final String messagingConnectPartnerMessageId;
    private final String messageId;
    private final String from;
    private final String to;

    /**
     * Creates an instance of SmsDeliveryReport.
     *
     * @param messageId                        The identifier of the outgoing
     *                                         message.
     * @param from                             The sender's phone number in E.164
     *                                         format.
     * @param to                               The recipient's phone number in E.164
     *                                         format.
     * @param deliveryStatus                   The delivery status of the message.
     * @param deliveryStatusDetails            Detailed information about the
     *                                         delivery status.
     * @param deliveryAttempts                 Array of delivery attempts made for
     *                                         this message.
     * @param receivedTimestamp                The timestamp when the delivery
     *                                         report was received.
     * @param tag                              Custom tag provided when sending the
     *                                         message.
     * @param messagingConnectPartnerMessageId Message ID generated by Messaging
     *                                         Connect Partner.
     */
    public SmsDeliveryReport(String messageId, String from, String to, SmsDeliveryStatus deliveryStatus,
        String deliveryStatusDetails, List<SmsDeliveryAttempt> deliveryAttempts, OffsetDateTime receivedTimestamp,
        String tag, String messagingConnectPartnerMessageId) {
        this.messageId = messageId;
        this.from = from;
        this.to = to;
        this.deliveryStatus = deliveryStatus;
        this.deliveryStatusDetails = deliveryStatusDetails;
        this.deliveryAttempts = deliveryAttempts;
        this.receivedTimestamp = receivedTimestamp;
        this.tag = tag;
        this.messagingConnectPartnerMessageId = messagingConnectPartnerMessageId;
    }

    /**
     * Gets the delivery status of the message.
     *
     * @return the delivery status of the message.
     */
    public SmsDeliveryStatus getDeliveryStatus() {
        return deliveryStatus;
    }

    /**
     * Gets detailed information about the delivery status.
     *
     * @return detailed information about the delivery status.
     */
    public String getDeliveryStatusDetails() {
        return deliveryStatusDetails;
    }

    /**
     * Gets the array of delivery attempts made for this message.
     *
     * @return the array of delivery attempts made for this message.
     */
    public List<SmsDeliveryAttempt> getDeliveryAttempts() {
        return deliveryAttempts;
    }

    /**
     * Gets the timestamp when the delivery report was received.
     *
     * @return the timestamp when the delivery report was received.
     */
    public OffsetDateTime getReceivedTimestamp() {
        return receivedTimestamp;
    }

    /**
     * Gets the custom tag provided when sending the message.
     *
     * @return the custom tag provided when sending the message.
     */
    public String getTag() {
        return tag;
    }

    /**
     * Gets the message ID generated by Messaging Connect Partner.
     * Only present for delivery reports created during Messaging Connect flow.
     *
     * @return the message ID generated by Messaging Connect Partner.
     */
    public String getMessagingConnectPartnerMessageId() {
        return messagingConnectPartnerMessageId;
    }

    /**
     * Gets the identifier of the outgoing message.
     *
     * @return the identifier of the outgoing message.
     */
    public String getMessageId() {
        return messageId;
    }

    /**
     * Gets the sender's phone number in E.164 format.
     *
     * @return the sender's phone number in E.164 format.
     */
    public String getFrom() {
        return from;
    }

    /**
     * Gets the recipient's phone number in E.164 format.
     *
     * @return the recipient's phone number in E.164 format.
     */
    public String getTo() {
        return to;
    }
}
