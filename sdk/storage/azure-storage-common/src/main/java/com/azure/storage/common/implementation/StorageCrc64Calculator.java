    public static long mulX_N(long a, long uSize) {
        long i = 0;
        long r = a;

        while (uSize != 0) {
            if ((uSize & 1) == 1) {
                r = mulPolyUnrolled(r, m_uX2N[(int) i]) & 0xFFFFFFFFFFFFFFFFL; // Ensure result is treated as unsigned
            }
            uSize >>>= 1; // Unsigned right shift
            i += 1;
        }

        return r;
    }

