    public static long compute(byte[] src, long uCrc) {
        int pData = 0;
        long uSize = src.length;
        long uBytes, uStop;

        uCrc ^= ~0L; // Flip all bits of uCrc

        uBytes = 0;
        uStop = uSize - (uSize % 32);

        if (uStop >= 2 * 32) {
            long uCrc0 = 0L;
            long uCrc1 = 0L;
            long uCrc2 = 0L;
            long uCrc3 = 0L;

            int pLast = pData + (int) uStop - 32;
            uSize -= uStop;
            uCrc0 = uCrc;

            for (; pData < pLast; pData += 32) {
                long b0, b1, b2, b3;

                // Load and XOR data with CRC
                b0 = ByteBuffer.wrap(src, pData, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc0;
                b1 = ByteBuffer.wrap(src, pData + 8, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc1;
                b2 = ByteBuffer.wrap(src, pData + 16, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc2;
                b3 = ByteBuffer.wrap(src, pData + 24, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc3;

                // Unsigned updates using tables and masking
                uCrc0 = m_u32[7 * 256 + ((int) (b0 & 0xFF))];
                b0 >>>= 8;
                uCrc1 = m_u32[7 * 256 + ((int) (b1 & 0xFF))];
                b1 >>>= 8;
                uCrc2 = m_u32[7 * 256 + ((int) (b2 & 0xFF))];
                b2 >>>= 8;
                uCrc3 = m_u32[7 * 256 + ((int) (b3 & 0xFF))];
                b3 >>>= 8;

                // Repeat for remaining bytes
                for (int i = 6; i >= 0; i--) {
                    uCrc0 ^= m_u32[i * 256 + ((int) (b0 & 0xFF))];
                    b0 >>>= 8;
                    uCrc1 ^= m_u32[i * 256 + ((int) (b1 & 0xFF))];
                    b1 >>>= 8;
                    uCrc2 ^= m_u32[i * 256 + ((int) (b2 & 0xFF))];
                    b2 >>>= 8;
                    uCrc3 ^= m_u32[i * 256 + ((int) (b3 & 0xFF))];
                    b3 >>>= 8;
                }
            }

            // Combine CRC values
            uCrc = 0;
            uCrc ^= ByteBuffer.wrap(src, pData, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc0;
            uCrc = updateCrcWithTables(uCrc);

            uCrc ^= ByteBuffer.wrap(src, pData + 8, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc1;
            uCrc = updateCrcWithTables(uCrc);

            uCrc ^= ByteBuffer.wrap(src, pData + 16, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc2;
            uCrc = updateCrcWithTables(uCrc);

            uCrc ^= ByteBuffer.wrap(src, pData + 24, 8).order(ByteOrder.LITTLE_ENDIAN).getLong() ^ uCrc3;
            uCrc = updateCrcWithTables(uCrc);

            pData += 32;
        }

        // Process remaining bytes
        for (uBytes = 0; uBytes < uSize; ++uBytes, ++pData) {
            uCrc = (uCrc >>> 8) ^ m_u1[(int) ((uCrc ^ src[pData]) & 0xFF)];
        }

        return uCrc ^ ~0L; // Final bit flip
    }

    // Helper method to update CRC using lookup tables
    private static long updateCrcWithTables(long uCrc) {
        for (int i = 0; i < 8; i++) {
            uCrc = (uCrc >>> 8) ^ m_u1[(int) (uCrc & 0xFF)];
        }
        return uCrc;
    }

    public static long concat(long uInitialCrcAB, long uInitialCrcA, long uFinalCrcA, long uSizeA, long uInitialCrcB,
        long uFinalCrcB, long uSizeB) {
        long uFinalCrcAB = uFinalCrcA ^ ~0L; // Invert bits for unsigned logic

        // Ensure unsigned behavior when comparing uInitialCrcA and uInitialCrcAB
        if ((uInitialCrcA & 0xFFFFFFFFFFFFFFFFL) != (uInitialCrcAB & 0xFFFFFFFFFFFFFFFFL)) {
            // Apply mulX_N with proper unsigned masking
            uFinalCrcAB ^= mulX_N((uInitialCrcA ^ uInitialCrcAB) & 0xFFFFFFFFFFFFFFFFL, uSizeA) & 0xFFFFFFFFFFFFFFFFL;
        }

        uFinalCrcAB ^= (uInitialCrcB ^ ~0L) & 0xFFFFFFFFFFFFFFFFL; // Ensure unsigned XOR logic
        uFinalCrcAB = mulX_N(uFinalCrcAB & 0xFFFFFFFFFFFFFFFFL, uSizeB) & 0xFFFFFFFFFFFFFFFFL;
        uFinalCrcAB ^= uFinalCrcB & 0xFFFFFFFFFFFFFFFFL;

        return uFinalCrcAB & 0xFFFFFFFFFFFFFFFFL; // Ensure the result is treated as unsigned
    }

